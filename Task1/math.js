// округления
const number = 3.54

// округляет в сторону меньшего числа
console.log(Math.floor(number));

// округляет в сторону большего числа
console.log(Math.ceil(number));

// округляет по правилам математики
console.log(Math.round(number));

// Модуль от числа
console.log(Math.abs(1))
console.log(Math.abs(-1))

// генерация рандомных чисел от 0 до 1
console.log(Math.random());

// корень квадратный
console.log(Math.sqrt(9))